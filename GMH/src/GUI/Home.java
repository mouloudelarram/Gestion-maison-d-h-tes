    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (FirstNameClient.getText().isEmpty() || LastNameClient.getText().isEmpty() || EmailClient.getText().isEmpty() || phoneclient.getText().isEmpty() || CIN.getText().isEmpty() || OffreId.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Erreur Please Fill all the Information");
        }
        else {
            if (new TableOffer().checkIfIdExist(Integer.parseInt(OffreId.getText()), typeDemande)){
                if (!(new TableOffer().statusTableOffer(Integer.parseInt(OffreId.getText())))){                
                    try{
                        new TableClient().pushTableClient(FirstNameClient.getText(), LastNameClient.getText(), EmailClient.getText(), phoneclient.getText(), CIN.getText());
                        JOptionPane.showMessageDialog(null,"Client added succesfuly.");
                        new TableDemande().pushTableDemande(new TableClient().getIdclientTableClient(CIN.getText()), Integer.parseInt(OffreId.getText()), this.IdAdmin, typeDemande);

                        FirstNameClient.setText(""); 
                        LastNameClient.setText(""); 
                        EmailClient.setText("");
                        phoneclient.setText(""); 
                        CIN.setText("");
                        new TableOffer().editStatusTableOffer(Integer.parseInt(OffreId.getText()));
                        OffreId.setText("");
                        //afficheTableClient(new TableClient().getTableClient());  
                        afficheTableDemande(new TableDemande().getTableDemande());
                        afficheTableChembre(new TableOffer().getTableOffer("chambre"));
                        afficheTableTable(new TableOffer().getTableOffer("table"));
                    }catch(Exception e){
                        JOptionPane.showMessageDialog(null,"Information Already Exist");
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"This offre is already occuped.");
                }
            }else{
                    JOptionPane.showMessageDialog(null,"May be there is an error in the offer Id ");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

